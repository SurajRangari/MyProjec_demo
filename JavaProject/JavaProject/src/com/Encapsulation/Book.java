package com.Encapsulation;

public class Book {
	
	int id;
	String name;
	int price;
	
	public void setId(int id)
	{
		this.id=id;
	}
	public void setName(String name)
	{
		this.name=name;
	}
	public void setPrice(int price)
	{
		this.price=price;
	}
	public int getId()
	{
		return id;
	}
	public String getName()
	{
		return name;
	}
	public int getPrice()
	{  
		
		return price;
	}

}
