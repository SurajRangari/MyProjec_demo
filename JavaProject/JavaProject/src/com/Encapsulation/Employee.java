package com.Encapsulation;

public class Employee {     
	private int id;
	private String name;
	private int salery;
	
	public void setId(int id)
	{
		this.id=id;
	}
	public void setName(String name)
	{
		this.name=name;
	}
	public void setSalary(int salary)
	{
		this.salery=salery;
	}
	public int getId()
	{
		return id;
	}
	public String getName()
	{
		return name;
	}
	public  int getSalery()
	{
		return salery;
	}
	
	
	
		
		
	

}
